const selectElement = document.getElementById('user-categories');
const displayContainer = document.getElementById('selected-categories-display');
const noSelectionMessage = document.getElementById('no-selection-message');

function updateSelectedTags() {
  displayContainer.innerHTML = '';
  const selectedOptions = Array.from(selectElement.options).filter(opt => opt.selected && opt.value !== '');

  if (selectedOptions.length === 0) {
    noSelectionMessage.style.display = 'inline';
  } else {
    noSelectionMessage.style.display = 'none';

    selectedOptions.forEach(option => {
      const tag = document.createElement('span');
      tag.className = 'bg-blue-600 text-white px-3 py-1 rounded-full text-sm flex items-center gap-2';
      tag.textContent = option.text;

      const removeBtn = document.createElement('button');
      removeBtn.innerHTML = '&times;';
      removeBtn.className = 'ml-1 text-white hover:text-red-400';
      removeBtn.onclick = () => {
        option.selected = false;
        updateSelectedTags();
      };

      tag.appendChild(removeBtn);
      displayContainer.appendChild(tag);
    });
  }
}

